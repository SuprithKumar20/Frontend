//  assignment operators
// = assignment
// += addition assignment
// -= subtraction assignment
// *= multiplication assignment
// /= division assignment
// %= modulus assignment
let num0=5
console.log(num0)   // 5
console.log(num0++) // 5
console.log(num0)   // 6
console.log(++num0) // 7
console.log(++num0) // 8

let num=10
num=num+5
console.log(num) // 15

let num1=10
num1-=5
console.log(num1) // 5
 
let num2=10
num2*=5
console.log(num2) // 50

let num3=10
num3/=5
console.log(num3) // 2

let num4=10
num4%=5
console.log(num4) // 0
 
