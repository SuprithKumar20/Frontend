//*    Object-Literals

// SYNTAX:
//      var ObjectName = {
//          key : value,
//          key : value,
//          key : value
//      }

//: EXAMPLE 1:

var person = {
    name : "Deepak C S",
    age : 22,
    city : "Bengaluru"
};

console.log(person); // { name: 'Deepak C S', age: 22, city: 'Bengaluru' }
console.log(person.name); // Deepak C S
console.log(person.age);    // 22
console.log(person.city);   // Bengaluru    